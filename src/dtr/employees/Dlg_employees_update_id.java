    private void ok() {
        int old_id = FitIn.toInt(jTextField1.getText());
        int new_id = FitIn.toInt(jTextField2.getText());
        if (new_id == old_id || new_id <= 0) {
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(new_id));
        }
    }
